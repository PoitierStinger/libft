#include "libft.h"

char *ft_strchr(const char *s, int c)
{
	char	cc = c;
	
	while (*s || *s == '\0')
	{
		if (*s == cc)
		{
			
			return ((char *)s);
		}
		if (*s == '\0')
		{
			return (NULL);
		}
		s++;
	}
	return(0);
}
